export const LANG_KEY = "domstudio_lang";
const SUPPORTED_LANGS = new Set(["ru", "en"]);
const RU_TIMEZONES = new Set([
  "Europe/Kaliningrad",
  "Europe/Moscow",
  "Europe/Samara",
  "Asia/Yekaterinburg",
  "Asia/Omsk",
  "Asia/Novosibirsk",
  "Asia/Krasnoyarsk",
  "Asia/Irkutsk",
  "Asia/Yakutsk",
  "Asia/Vladivostok",
  "Asia/Magadan",
  "Asia/Kamchatka",
]);

function normalizeLang(lang) {
  const base = String(lang || "").toLowerCase().split("-")[0];
  return SUPPORTED_LANGS.has(base) ? base : null;
}

export function isRussianMarket() {
  const languages = [
    ...(navigator.languages || []),
    navigator.language,
  ].filter(Boolean).map((lang) => String(lang).toLowerCase());
  const timezone = Intl.DateTimeFormat().resolvedOptions().timeZone;
  return RU_TIMEZONES.has(timezone) || languages.some((lang) => lang === "ru" || lang.startsWith("ru-") || lang.endsWith("-ru"));
}

function detectLang() {
  return isRussianMarket() ? "ru" : "en";
}

export function getLang() {
  const saved = normalizeLang(localStorage.getItem(LANG_KEY));
  return saved || detectLang();
}

export function setLang(lang) {
  localStorage.setItem(LANG_KEY, normalizeLang(lang) || "ru");
}

